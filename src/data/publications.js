  export function findRelevantPublications(query, limit = 4) {
    // I normalize the query so matching is case-insensitive.
    const q = query.toLowerCase().trim();
  
    if (!q) return [];
  
    // I define lightweight keyword boosts for common visitor intents.
    const keywordGroups = [
      ["eye-tracking", "eye tracking", "gaze", "fixation", "scanpath", "mobile eye-tracking", "mobile eye tracking"],
      ["museum", "gallery", "barnes", "art museum", "visitor", "salon hang"],
      ["digital", "online", "screen", "virtual", "vr"],
      ["aesthetic", "aesthetics", "beauty", "liking", "art engagement", "aesthetic experience"],
      ["public", "mural", "murals", "neighborhood", "well-being", "wellbeing"],
      ["therapy", "art therapy", "ptss", "emotion", "military"],
      ["database", "repository", "normed", "stimuli"],
      ["neuroscience", "reward", "aversion", "oleamide", "cb1", "5ht2c", "feeding"],
      ["collaboration", "methods", "projects"]
    ];
  
    const queryTerms = new Set(
      q
        .split(/[\s,.;:!?()]+/)
        .map((term) => term.trim())
        .filter(Boolean)
    );
  
    return publications
      .map((pub) => {
        // I build one searchable text block from the publication fields.
        const haystack = [
          pub.title,
          pub.journal,
          pub.summary,
          ...(pub.topics || []),
          pub.recommendedFor || "",
          ...(pub.authors || [])
        ]
          .join(" ")
          .toLowerCase();
  
        let score = 0;
  
        // I reward direct term matches.
        for (const term of queryTerms) {
          if (term.length < 3) continue;
          if (haystack.includes(term)) score += 2;
        }
  
        // I reward phrase matches from intent groups.
        for (const group of keywordGroups) {
          const matchedInQuery = group.some((term) => q.includes(term));
          if (!matchedInQuery) continue;
  
          for (const term of group) {
            if (haystack.includes(term)) {
              score += 3;
            }
          }
        }
  
        // I give extra weight to title matches.
        if (pub.title.toLowerCase().includes(q)) score += 6;
  
        return { pub, score };
      })
      .filter((item) => item.score > 0)
      .sort((a, b) => b.score - a.score)
      .slice(0, limit)
      .map((item) => item.pub);
  }